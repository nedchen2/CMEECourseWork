#include <stdio.h>

int main(void)
{
    char c;
    char b;

    c = 'A';
    b = 'a';

    printf("The value of c: %c\n", c);
    printf("The value of c as an int: %i\n", c);

    printf("The character a represented as an integer: %i\n", b);
    printf("The character representation of 97: %c\n", 97);

    return 0;
}
